import Payment from "../models/payment.model.js";
import AppError from "../utils/error.util.js";
import User from "../models/user.model.js";
import crypto from "crypto";
import { razorpay } from '../server.js';

const getRazorpayApiKey = async(req,res,next) => {
    try{
        res.status(200).json({
            success: true,
            message: 'Razarpay API key',
            key: process.env.RAZORPAY_KEY_ID
        });
    }
    catch(e){
        return next(
            new AppError(e.message, 500)
        )
    }
}

const buySubscription = async(req,res,next) => {
    try{
        const {id} = req.user;
        const user = await User.findById(id);

        if(!user){ // although will check in routes-middleware still one more check
            return next(
                new AppError('Unauthorized, please login',400)
            )
        }

        if(user.role === 'ADMIN'){
            return next(
                new AppError('Admin cannot purchase a subscription',400)
            )
        }
    
        // Creating a subscription using razorpay that we imported from the server.js
        const subscription = await razorpay.subscriptions.create({
            plan_id: process.env.RAZORPAY_PLAN_ID, // The unique plan ID
            customer_notify: 1, // 1 means razorpay will handle notifying the customer, 0 means we will not notify the customer
            total_count: 10, // Subscription plan will occur 10 times
        });
    
        // Adding the ID and the status to the user account
        user.subscription.id = subscription.id;
        user.subscription.status = subscription.status;
    
        await user.save();
    
        res.status(200).json({
            success: true,
            message: 'Subscribed Successfully',
            subscription_id: subscription.id
        });
    }
    catch(e){
        return next(
            new AppError(e.message, 400)
        )
    }
}

const verifySubscription = async(req,res,next) => {
    try{
        const {id} = req.user;
    
        // User does NOT enter these values manually: They are automatically generated by Razorpay and sent to your backend after payment.
        const {razorpay_payment_id, razorpay_signature, razorpay_subscription_id} = req.body;
    
        const user = await User.findById(id);
    
        if(!user){ // although will check in routes-middleware still one more check
            return next(
                new AppError('Unauthorized, please login',400)
            )
        }
    
        const subscriptionId = user.subscription.id;
    
        // This is how generate signature
        const generatedSignature = crypto 
            .createHmac('sha256', process.env.RAZORPAY_SECRET)
            .update(`${razorpay_payment_id}|${subscriptionId}`)
            .digest('hex');
    
        if(generatedSignature !== razorpay_signature){
            return next(new AppError('Payment not verified, please try again', 500))
        }
    
        await Payment.create({
            razorpay_payment_id,
            razorpay_subscription_id,
            razorpay_signature
        })
    
        user.subscription.status = 'active';
    
        await user.save();
    
        res.status(200).json({
            success: true,
            message: 'Payment verified successfully!'
        });
    }
    catch(e){
        return next(
            new AppError(e.message, 500)
        )
    }
}

const cancelSubscription = async(req,res,next) => {
    
        const {id} = req.user;
        const user = await User.findById(id);
    
        if(!user){ // although will check in routes-middleware still one more check
            return next(
                new AppError('Unauthorized, please login',400)
            )
        }
    
        if(user.role === 'ADMIN'){
            return next(
                new AppError('Admin cannot purchase a subscription',400)
            )
        }

        const subscriptionId = user.subscription.id;
        try{

            const subscription = await razorpay.subscriptions.cancel(
                subscriptionId
            )
            user.subscription.status = subscription.status;
            
            await user.save();
            
        } 
        catch(e){ return next( new AppError(e.message, 500) ) }

        user.subscription.id = undefined; // Remove the subscription ID from user DB
        user.subscription.status = undefined; // Change the subscription Status in user DB

        await user.save();

        // Send the response
        res.status(200).json({
            success: true,
            message: 'Subscription canceled successfully',
        });
}

const allPayments = async(req,res,next) => {
    try{

        // getting no. of payments
        let allItems = [];
        let skip = 0;
        const count = 100;

        while (true) {
            const response = await razorpay.payments.all({
                count,
                skip,
            });

            const items = response.items;

            allItems = [...allItems, ...items];

            if (items.length < count) break;

            skip += count;
        }

        // Find all subscriptions from razorpay
        const allPayments = await razorpay.payments.all({
            count: count ? count : 10, // If count is sent then use that else default to 10
            skip: skip ? skip : 0, // // If skip is sent then use that else default to 0
        });

        const monthNames = [
            'January',
            'February',
            'March',
            'April',
            'May',
            'June',
            'July',
            'August',
            'September',
            'October',
            'November',
            'December',
        ];

        const finalMonths = {
            January: 0,
            February: 0,
            March: 0,
            April: 0,
            May: 0,
            June: 0,
            July: 0,
            August: 0,
            September: 0,
            October: 0,
            November: 0,
            December: 0,
        };

        const monthlyWisePayments = allPayments.items.map((payment) => {
            // We are using payment.start_at which is in unix time, so we are converting it to Human readable format using Date()
            const monthsInNumbers = new Date(payment.created_at* 1000);

            return monthNames[monthsInNumbers.getMonth()];
        });

        monthlyWisePayments.map((month) => {
            Object.keys(finalMonths).forEach((objMonth) => {
            if (month === objMonth) {
                finalMonths[month] += 1;
            }
            });
        });

        const monthlySalesRecord = [];

        Object.keys(finalMonths).forEach((monthName) => {
            monthlySalesRecord.push(finalMonths[monthName]);
        });

        res.status(200).json({
            success: true,
            message: 'All payments',
            allPayments,
            finalMonths,
            monthlySalesRecord,
        });
    }
    catch(e){ return next(new AppError(e.message, 500))}

}

